COMMENT ON TABLE EVALUATION_SCORE_HISTORY IS '평가 항목';
COMMENT ON COLUMN EVALUATION_SCORE_HISTORY.EVALUATION_NO IS '항목 식별번호';
COMMENT ON COLUMN EVALUATION_SCORE_HISTORY.ITEM_NO IS '항목';
COMMENT ON COLUMN EVALUATION_SCORE_HISTORY.SCORE IS '점수';
COMMENT ON COLUMN EVALUATION_SCORE_HISTORY.RESON IS '이유';
COMMENT ON COLUMN EVALUATION_SCORE_HISTORY.ACTION_AT IS '변경 시각';
COMMENT ON COLUMN EVALUATION_SCORE_HISTORY.ACTION_BY IS '변경자';
COMMENT ON COLUMN EVALUATION_SCORE_HISTORY.ACTION_RESON IS '변경 이유';


CREATE TABLE FILE_INFO(
  FILE_NO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ORIGINAL_NAME VARCHAR2(330) NOT NULL,
  CHANGED_NAME VARCHAR2(50) NOT NULL,
  MIME VARCHAR2(50) NOT NULL,
  FILE_SIZE NUMBER NOT NULL,
  SAVE_PATH VARCHAR2(50) NOT NULL,
  EXPIRE_ON DATE
);
COMMENT ON TABLE FILE_INFO IS '파일';
COMMENT ON COLUMN FILE_INFO.FILE_NO IS '파일 식별번호';
COMMENT ON COLUMN FILE_INFO.ORIGINAL_NAME IS '원본 이름';
COMMENT ON COLUMN FILE_INFO.CHANGED_NAME IS '변경된 이름';
COMMENT ON COLUMN FILE_INFO.MIME IS 'MIME 타입';
COMMENT ON COLUMN FILE_INFO.FILE_SIZE IS '크기';
COMMENT ON COLUMN FILE_INFO.SAVE_PATH IS '저장 경로';
COMMENT ON COLUMN FILE_INFO.EXPIRE_ON IS '만료일';


CREATE TABLE FILE_HISTORY(
  HISTORY_NO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  FILE_NO NUMBER REFERENCES FILE_INFO(FILE_NO) ON DELETE SET NULL,
  ORIGINAL_NAME VARCHAR2(330) NOT NULL,
  CHANGED_NAME VARCHAR2(50) NOT NULL,
  SAVE_PATH VARCHAR2(50) NOT NULL,
  ACTION VARCHAR2(10) NOT NULL,
  ACTION_AT DATE DEFAULT SYSDATE,
  ACTION_BY NUMBER REFERENCES MEMBER(MEMBER_NO)
);
COMMENT ON TABLE FILE_HISTORY IS '파일 기록';
COMMENT ON COLUMN FILE_HISTORY.FILE_NO IS '파일 식별번호';
COMMENT ON COLUMN FILE_HISTORY.ORIGINAL_NAME IS '원본 이름';
COMMENT ON COLUMN FILE_HISTORY.CHANGED_NAME IS '변경된 이름';
COMMENT ON COLUMN FILE_HISTORY.SAVE_PATH IS '저장 경로';
COMMENT ON COLUMN FILE_HISTORY.ACTION IS '변경 상태';
COMMENT ON COLUMN FILE_HISTORY.ACTION_AT IS '변경 시각';
COMMENT ON COLUMN FILE_HISTORY.ACTION_BY IS '변경자';


