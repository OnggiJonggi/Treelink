CREATE TABLE MEMBER(
  MEMBER_NO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  ID VARCHAR2(300) NOT NULL,
  PWD VARCHAR2(300) NOT NULL,
  NAME VARCHAR2(300) NOT NULL,
  NICKNAME VARCHAR2(300) NOT NULL,
  PHONE VARCHAR2(15),
  STATUS VARCHAR2(10) DEFAULT 'ACTIVE' NOT NULL
);
COMMENT ON TABLE MEMBER IS '회원';
COMMENT ON COLUMN MEMBER.MEMBER_NO IS '아이디';
COMMENT ON COLUMN MEMBER.PWD IS '비밀번호';
COMMENT ON COLUMN MEMBER.NAME IS '이름';
COMMENT ON COLUMN MEMBER.NICKNAME IS '별명';
COMMENT ON COLUMN MEMBER.PHONE IS '연락처';
COMMENT ON COLUMN MEMBER.STATUS IS '상태';


CREATE TABLE ROLE_INFO(
  ROLE_NO NUMBER PRIMARY KEY,
  NAME VARCHAR2(30) NOT NULL
);
COMMENT ON TABLE ROLE_INFO IS '권한';
COMMENT ON COLUMN ROLE_INFO.ROLE_NO IS '권한 식별번호';
COMMENT ON COLUMN ROLE_INFO.NAME IS '권한 이름';


CREATE TABLE MEMBER_ROLE(
  MEMBER_NO NUMBER REFERENCES MEMBER(MEMBER_NO),
  ROLE_NO NUMBER REFERENCES ROLE_INFO(ROLE_NO)
);
COMMENT ON TABLE MEMBER_ROLE IS '회원-권한';
COMMENT ON COLUMN MEMBER_ROLE.MEMBER_NO IS '회원번호';
COMMENT ON COLUMN MEMBER_ROLE.ROLE_NO IS '권한 식별번호';


CREATE TABLE COMPANY(
  COMPANY_NO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  BUSINESS_NO VARCHAR2(15) NOT NULL UNIQUE,
  COMPANY_NAME VARCHAR2(330) NOT NULL,
  REPRESENTATIVE_NAME VARCHAR2(33) NOT NULL,
  PHONE VARCHAR2(15) NOT NULL,
  EMAIL VARCHAR2(100),
  CREATED_ON DATE NOT NULL,
  INTRO CLOB,
  STATUS VARCHAR2(10) DEFAULT 'ACTIVE' NOT NULL
);
COMMENT ON TABLE COMPANY IS '업체';
COMMENT ON COLUMN COMPANY.COMPANY_NO IS '업체 식별번호';
COMMENT ON COLUMN COMPANY.BUSINESS_NO IS '사업자 등록번호';
COMMENT ON COLUMN COMPANY.COMPANY_NAME IS '업체 이름';
COMMENT ON COLUMN COMPANY.REPRESENTATIVE_NAME IS '대표 이름';
COMMENT ON COLUMN COMPANY.PHONE IS '전화번호';
COMMENT ON COLUMN COMPANY.EMAIL IS '이메일';
COMMENT ON COLUMN COMPANY.CREATED_ON IS '창립일';
COMMENT ON COLUMN COMPANY.INTRO IS '소개문';
COMMENT ON COLUMN COMPANY.STATUS IS '상태';


CREATE TABLE ADDRESS(
  ADDRESS_NO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  LATITUDE NUMBER,
  LONGITUDE NUMBER,
  ROAD_NAME VARCHAR2(330),
  SIDO VARCHAR2(70),
  SIGUNGU VARCHAR2(70)
);
COMMENT ON TABLE ADDRESS IS '업체 주소';
COMMENT ON COLUMN ADDRESS.ADDRESS_NO IS '업체 식별번호';
COMMENT ON COLUMN ADDRESS.LATITUDE IS '위도';
COMMENT ON COLUMN ADDRESS.LONGITUDE IS '경도';
COMMENT ON COLUMN ADDRESS.ROAD_NAME IS '도로명주소';
COMMENT ON COLUMN ADDRESS.SIDO IS '시/도';
COMMENT ON COLUMN ADDRESS.SIGUNGU IS '시/군/구';


CREATE TABLE COMPANY_ADDRESS(
  COMPANY_NO NUMBER REFERENCES COMPANY(COMPANY_NO) NOT NULL,
  ADDRESS_NO NUMBER REFERENCES ADDRESS(ADDRESS_NO) NOT NULL
);
COMMENT ON TABLE COMPANY_ADDRESS IS '업체-주소';
COMMENT ON COLUMN COMPANY_ADDRESS.COMPANY_NO IS '업체 식별번호';
COMMENT ON COLUMN COMPANY_ADDRESS.ADDRESS_NO IS '주소 식별번호';


CREATE TABLE SPECIALTY(
  SPECIALTY_NO NUMBER(2) PRIMARY KEY,
  FIELD VARCHAR2(70) NOT NULL
);
COMMENT ON TABLE SPECIALTY IS '주 종목';
COMMENT ON COLUMN SPECIALTY.SPECIALTY_NO IS '종목 식별번호';
COMMENT ON COLUMN SPECIALTY.FIELD IS '종목';


CREATE TABLE COMPANY_SPECIALTY(
  COMPANY_NO NUMBER REFERENCES COMPANY(COMPANY_NO) NOT NULL,
  SPECIALTY_NO NUMBER REFERENCES SPECIALTY(SPECIALTY_NO) NOT NULL,
